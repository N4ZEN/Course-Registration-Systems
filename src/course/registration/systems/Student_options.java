        String csearch = search.getText();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(table);
        tblcoursess.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(csearch));

