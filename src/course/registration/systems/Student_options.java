    private void btnstudentsaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnstudentsaveMouseClicked
        // TODO add your handling code here:
        String filpath = "C:/Users/Naza Zuhair/Documents/NetBeansProjects/Course Registration Systems/Student.txt";
        String tempt ="C:/Users/Naza Zuhair/Documents/NetBeansProjects/Course Registration Systems/Tempt.txt";
        File nwfile = new File(tempt);
        File odfile = new File(filpath);

        String studentname, studentid, studentemail, studentlevel, studentno;

        studentname = stnname.getText();
        studentid=stnid.getText();
        studentno= stnphno.getText();
        studentemail =stnemail.getText();
        studentlevel = stnlvl.getText();
        

        try{
            FileWriter fwr = new FileWriter(tempt,true);
            BufferedWriter bwr = new BufferedWriter(fwr);
            bwr.write(studentname+","+studentid+","+studentlevel+","+studentemail+","+studentno);
            bwr.write("\r\n");

            bwr.close();
            fwr.close();
            odfile.delete();
            File dups = new File(filpath);
            nwfile.renameTo(dups);

            JOptionPane.showMessageDialog(this, "Student Details Updated Successfully! ");
            stnname.setEditable(false);
            stnid.setEditable(false);
            stnphno.setEditable(false);
            stnlvl.setEditable(false);
            stnemail.setEditable(false);
            btnstudentsave.setVisible(false);
            studentdetails.setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Error");
        }

    }//GEN-LAST:event_btnstudentsaveMouseClicked

